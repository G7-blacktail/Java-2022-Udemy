package estruturas_de_repeticao.break_continue;

public class BreakLeanning {
    public static void main(String[] args) {
        
        for (int i = 0; i < 10; i++){
            if(i == 5){
                break;
            }
            System.out.println(i);
        }

        System.out.println("FiM");
    }
}
